def addition(x,y):
    add=x+y
    print("\nThe addition is:",add)

def subtraction(x,y):
    sub=x-y
    print("\nThe subtraction is:",sub)

def multiplication(x,y):
    mult=x*y
    print("\nThe multiplication is:",mult)

def division(x,y):
    div=x/y
    print("\nThe division is:",div)

def power(x,y):
    pw = x**y
    print("\nThe power is:",pw)


